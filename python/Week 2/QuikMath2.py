def Add(addend1, addend2):
    return addend1 + addend2

def Subtract(subtractend1, subtractend2):
    return subtractend1 - subtractend2

def Multiply(multiply1, multiply2):
    return multiply1 * multiply2

def Divde(divide1, divide2):
    return divide1 / divide2


sum = Add(5, 35)
difference = Subtract(10,90)
double = Multiply(12, 9)
take = Divde(144, 12)

